package pt.ist.rest.service.dto;

/**
 * The Class PlateQuantityDto.
 */
public class PlateQuantityDto
        implements java.io.Serializable {

    /**
     * The Constant serialVersionUID.
     */
    private static final long serialVersionUID = 8161126757385090701L;

    private PlateDto plateDto;
    private int quantity;

    /**
     * Instantiates a new plate quantity dto.
     * 
     * @param plate the plate
     * @param quantity the quantity
     */
    public PlateQuantityDto(PlateDto plate, int quantity) {
        plateDto = plate;
        this.quantity = quantity;
    }
    
    public PlateQuantityDto() { }

    /**
     * Gets the plate dto.
     * 
     * @return the plate dto
     */
    public PlateDto getPlateDto() {
        return plateDto;
    }

    /**
     * Gets the quantity.
     * 
     * @return the quantity
     */
    public int getQuantity() {
        return quantity;
    }
}

package pt.ist.rest.service.dto;

import pt.ist.rest.Utilities.FoodType;

/**
 * The Class SimplePlateDto.
 */
public class SimpleFoodDto implements java.io.Serializable {

    /**
     * The Constant serialVersionUID.
     */
    private static final long serialVersionUID = -9028540478262406746L;

    /** The name. */
    protected String name;
    /** The type. */
    protected FoodType type;

    public SimpleFoodDto(String name, FoodType type) {
        this.name = name;
        this.type = type;
    }

    public SimpleFoodDto() { }

    /**
     * Gets the name.
     * 
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Gets the type.
     * 
     * @return the type
     */
    public FoodType getType() {
        return type;
    }

    /*
     * (non-Javadoc)
     * 
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
        return name + ", type: " + type;
    }
}
